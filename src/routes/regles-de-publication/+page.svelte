<div class="flex items-center justify-center text-center">
	<h1 class="py-10">Règles de Publication</h1>
</div>

<div
	class="mx-auto mb-10 max-w-[100%] rounded-lg bg-white px-6 sm:max-w-[90%] md:px-10 lg:max-w-5xl lg:px-12"
	lang="fr"
>
	<section class="mb-6 sm:mb-8">
		<h2>Préambule</h2>
		<p class="overflow-wrap-anywhere hyphens-auto break-words text-justify">
			Notre site est une plateforme dédiée au partage de connaissances et d'informations sur les
			montres. Nous souhaitons maintenir un environnement respectueux, informatif et de qualité pour
			tous nos utilisateurs. Les présentes règles de publication définissent les conditions à
			respecter pour soumettre un article sur notre plateforme.
		</p>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>1. Processus de publication</h2>
		<ol class="list-inside list-decimal">
			<li>
				Tout article soumis passe par un processus de modération avant d'être publié sur le site.
			</li>
			<li>
				Le statut de l'article reste "submitted" jusqu'à ce qu'un modérateur valide son contenu.
			</li>
			<li>Une fois approuvé, l'article est publié et accessible à tous les visiteurs du site.</li>
			<li>
				En cas de refus, l'auteur sera informé des raisons du rejet et pourra modifier son article
				pour le soumettre à nouveau.
			</li>
		</ol>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>2. Contenu autorisé</h2>
		<ol class="list-inside list-decimal">
			<li>
				Les articles doivent être en lien direct avec l'univers de l'horlogerie, des montres et
				accessoires associés.
			</li>
			<li>
				Les contenus autorisés incluent, sans s'y limiter :
				<ul class="list-inside list-disc pl-4 sm:pl-6">
					<li>Présentations et critiques de montres</li>
					<li>Analyses techniques</li>
					<li>Actualités du monde de l'horlogerie</li>
					<li>Guides d'achat et comparatifs</li>
					<li>Conseils d'entretien</li>
					<li>Histoire de l'horlogerie et des manufactures</li>
				</ul>
			</li>
		</ol>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>3. Contenu prohibé</h2>
		<ol class="list-inside list-decimal">
			<li>
				Les contenus suivants sont strictement interdits :
				<ul class="list-inside list-disc pl-4 sm:pl-6">
					<li>Contenu à caractère pornographique, obscène ou indécent</li>
					<li>Propos diffamatoires, injurieux ou incitant à la haine</li>
					<li>
						Contenus discriminatoires basés sur la race, le sexe, la religion, la nationalité, le
						handicap, l'orientation sexuelle ou l'âge
					</li>
					<li>Publications faisant la promotion d'activités illégales</li>
					<li>Informations personnelles concernant des tiers sans leur consentement</li>
					<li>Publicité déguisée ou non identifiée comme telle</li>
					<li>Contenu portant atteinte aux droits de propriété intellectuelle</li>
					<li>Articles contenant des liens vers des sites malveillants</li>
				</ul>
			</li>
		</ol>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>4. Qualité éditoriale</h2>
		<ol class="list-inside list-decimal">
			<li>
				Les articles doivent respecter un minimum de qualité éditoriale :
				<ul class="list-inside list-disc pl-4 sm:pl-6">
					<li>Être rédigés dans un français correct</li>
					<li>Ne pas contenir de fautes d'orthographe ou de grammaire excessives</li>
					<li>Présenter une structure claire et cohérente</li>
					<li>Fournir des informations vérifiables et exactes</li>
				</ul>
			</li>
			<li>Les sources doivent être citées lorsque des informations précises sont avancées.</li>
			<li>
				Les images utilisées doivent être de qualité convenable et libres de droits ou utilisées
				avec l'autorisation des ayants droit.
			</li>
		</ol>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>5. Droits d'auteur et propriété intellectuelle</h2>
		<ol class="list-inside list-decimal">
			<li>
				En soumettant un article, l'auteur garantit qu'il dispose des droits nécessaires sur
				l'ensemble du contenu (textes, images, etc.).
			</li>
			<li>
				L'auteur accorde au site une licence non exclusive, mondiale, gratuite, perpétuelle et
				irrévocable pour reproduire, afficher, exécuter, distribuer et créer des œuvres dérivées de
				son contenu.
			</li>
			<li>
				Le plagiat est strictement interdit. Tout contenu repris d'une source externe doit être
				clairement identifié comme citation et sa source doit être mentionnée.
			</li>
		</ol>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>6. Transparence et conflits d'intérêt</h2>
		<ol class="list-inside list-decimal">
			<li>
				Toute relation commerciale ou personnelle avec une marque ou un produit mentionné dans
				l'article doit être clairement divulguée.
			</li>
			<li>
				Si l'article contient des liens d'affiliation, ils doivent être identifiés comme tels.
			</li>
		</ol>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>7. Modification des articles publiés</h2>
		<ol class="list-inside list-decimal">
			<li>
				Une fois publié, un article peut être modifié par son auteur, mais les modifications
				substantielles seront soumises à une nouvelle modération.
			</li>
			<li>
				L'administration du site se réserve le droit de modifier ou supprimer tout contenu, à tout
				moment et sans préavis, si celui-ci ne respecte pas les présentes règles.
			</li>
		</ol>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>8. Sanctions en cas de non-respect</h2>
		<ol class="list-inside list-decimal">
			<li>
				Le non-respect des présentes règles peut entraîner :
				<ul class="list-inside list-disc pl-4 sm:pl-6">
					<li>Le refus de publication de l'article</li>
					<li>La suppression d'un article déjà publié</li>
					<li>La suspension temporaire du compte de l'utilisateur</li>
					<li>La suppression définitive du compte en cas de violations répétées</li>
				</ul>
			</li>
		</ol>
	</section>

	<section class="mb-6 sm:mb-8">
		<h2>9. Modification des règles</h2>
		<ol class="list-inside list-decimal">
			<li>
				Ces règles peuvent être modifiées à tout moment. Les utilisateurs seront informés des
				changements importants.
			</li>
			<li>
				Il appartient aux utilisateurs de consulter régulièrement les règles de publication en
				vigueur.
			</li>
		</ol>
	</section>

